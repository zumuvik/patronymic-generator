# Имена на -ий, подпадающие под стандартное правило (-евич/-евна)
# (словарь для алгоритмической обработки – те, кто не в EXCEPTIONS)
SUFFIX_RULES: Dict[str, Tuple[str, str, str]] = {
    # Суффикс: (нужно ли убрать окончание, суффикс сына, суффикс дочери)
    # Большинство имён
    "default": ("", "ович", "овна"),
    # Имена на -ий/-ей (кроме исключений)
    "iy": ("ий", "евич", "евна"),
    "ey": ("ей", "евич", "евна"),
    # Имена на -а/-я (кроме исключений)
    "a": ("а", "ич", "ична"),
    "ya": ("я", "ич", "ична"),
    # Имена на мягкий знак
    "ь": ("ь", "евич", "евна"),
}


def _normalize_name(name: str) -> str:
    """Приводит имя к стандартному виду: первая буква заглавная, остальные строчные."""
    if not name:
        return ""
    name = name.strip().capitalize()
    return name


def _detect_category(name: str) -> Optional[str]:
    """
    Определяет категорию имени по окончанию.
    Возвращает ключ категории или None.
    """
    if not name:
        return None
    if name.endswith("ий"):
        return "iy"
    if name.endswith("ей"):
        return "ey"
    if name.endswith("а"):
        return "a"
    if name.endswith("я"):
        return "ya"
    if name.endswith("ь"):
        return "ь"
    return "default"


def generate_patronymic(name: str) -> Dict[str, str]:
    """
    Генерирует отчество для сына и дочери по имени отца.

    Args:
        name: Имя отца в именительном падеже (например, "Александр")

    Returns:
        Словарь с ключами:
            - father_name: исходное имя
            - son_patronymic: отчество для сына
            - daughter_patronymic: отчество для дочери

    Raises:
        ValueError: если имя пустое или не поддерживается
    """
    normalized = _normalize_name(name)

    if not normalized:
        raise ValueError("Имя не может быть пустым")

    # Проверка: не содержатся ли не-русские символы (базовая)
    if not normalized.replace("ё", "е").isalpha():
        raise ValueError(f"Имя '{normalized}' содержит недопустимые символы")

    # 1. Проверка по словарю исключений
    if normalized in EXCEPTIONS:
        son, daughter = EXCEPTIONS[normalized]
        return {
            "father_name": normalized,
            "son_patronymic": son,
            "daughter_patronymic": daughter,
        }

    # 2. Определение категории
    category = _detect_category(normalized)
    if category is None:
        raise ValueError(f"Не удалось определить категорию для имени '{normalized}'")

    # 3. Применение правил
    if category in ("iy", "ey", "a", "ya", "ь"):
        suffix_info = SUFFIX_RULES[category]
        ending, son_suffix, daughter_suffix = suffix_info
        stem = normalized[: -len(ending)] if ending else normalized
        son = stem + son_suffix
        daughter = stem + daughter_suffix
    else:
        # "default" — твёрдый согласный
        son = normalized + "ович"
        daughter = normalized + "овна"

    return {
        "father_name": normalized,
        "son_patronymic": son,
        "daughter_patronymic": daughter,
    }


